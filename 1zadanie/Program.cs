Console.Write("Введите число A: ");
int A = Convert.ToInt32(Console.ReadLine());
Console.Write("Введите число B: ");
int B = Convert.ToInt32(Console.ReadLine());

if(A>B)
{
    Console.Write("Наибольшее число: ");
    Console.Write(A);
}
else
{
    Console.Write("Наибольшее число: ");
    Console.Write(B);
}